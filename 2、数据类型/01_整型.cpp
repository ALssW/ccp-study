#include <iostream>
using namespace std;

int main_201()
{
	// 短整型
	short num1 = 32797;
	cout << "num1 = " << num1 << endl; // num1 = -32738

	// 整型
	int num2 = 32798;
	cout << "num2 = " << num2 << endl; // num2 = 32798
	
	// 长整型
	long num3 = 10;
	cout << "num3 = " << num3 << endl;

	// 长长整型
	long long num4 = 10;
	cout << "num4 = " << num4 << endl;

	return 0;
}
