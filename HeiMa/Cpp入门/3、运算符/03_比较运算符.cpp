#include <iostream>
using namespace std;

int main_303()
{
	
	int a = 10;
	int b = 20;

	cout << "a = " << a << ", b = " << b << endl;
	cout << "a == b : " << (a == b) << endl; // 等于
	cout << "a != b : " << (a != b) << endl; // 不等于
	cout << "a > b : " << (a > b) << endl;   // 大于
	cout << "a < b : " << (a < b) << endl;   // 小于
	cout << "a >= b : " << (a >= b) << endl; // 大于等于
	cout << "a <= b : " << (a <= b) << endl; // 小于等于
	
	return 0;
}